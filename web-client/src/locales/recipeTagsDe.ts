// German labels for the recipe tag taxonomy (src/recipeTags.json), keyed by id.
// The JSON holds the English labels; these are the German equivalents resolved
// by tagLabel()/facetLabel() when the active language is German. Dish proper
// nouns (Sushi, Ramen, Tiramisu, …) are intentionally left unchanged.

// Resolve a tag/facet label for the active language: German from the maps below
// (falling back to the English label for anything not yet translated), English
// otherwise. `lang` is i18n.language so callers re-render on a language switch.
export function localizeTagLabel(id: string, fallback: string, lang: string): string {
  return lang.startsWith('de') ? recipeTagLabelsDe[id] ?? fallback : fallback
}

export function localizeFacetLabel(id: string, fallback: string, lang: string): string {
  return lang.startsWith('de') ? recipeFacetLabelsDe[id] ?? fallback : fallback
}

